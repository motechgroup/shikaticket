function loadPricingData(tierName) {
    currentTierName = tierName;
    fetch('<?php echo base_url('/api/marketing/packages'); ?>?account_type=travel_agency')
        .then(response => response.json())
        .then(data => {
            if (data.success && Array.isArray(data.packages)) {
                const found = data.packages.find(p => p.tier_name === tierName);
                if (found) {
                    currentPricing = { price_per_sms: parseFloat(found.price_per_sms), max_messages: parseInt(found.max_messages || '0', 10) || 0 };
                    displayPackageDetails();
                    return;
                }
            }
            console.error('Package not found or inactive for tier:', tierName);
            currentPricing = { price_per_sms: 2.50, max_messages: 1000 };
            displayPackageDetails();
        })
        .catch(error => {
            console.error('Error loading packages:', error);
            currentPricing = { price_per_sms: 2.50, max_messages: 1000 };
            displayPackageDetails();
        });
}

