function loadTravelDestinations() {
    const destinationSelect = document.getElementById('destinationId');
    
    // Clear existing options except the first one
    destinationSelect.innerHTML = '<option value="">Loading destinations...</option>';
    
    // Fetch real destinations from the server
    fetch('<?php echo base_url('/travel/destinations/api'); ?>')
        .then(response => response.json())
        .then(data => {
            destinationSelect.innerHTML = '<option value="">Choose a destination...</option>';
            
            if (data.success && data.destinations) {
                data.destinations.forEach(destination => {
                    const option = document.createElement('option');
                    option.value = destination.id;
                    option.textContent = destination.title;
                    option.dataset.departureDate = destination.departure_date ? destination.departure_date.split(' ')[0] : '';
                    destinationSelect.appendChild(option);
                });
                // Auto-fill dates when a destination is chosen
                destinationSelect.addEventListener('change', () => {
                    const sel = destinationSelect.options[destinationSelect.selectedIndex];
                    const depDate = sel ? (sel.dataset.departureDate || '') : '';
                    const startInput = document.getElementById('startDate');
                    const endInput = document.getElementById('endDate');
                    const todayStr = new Date().toISOString().split('T')[0];
                    const startVal = startInput.value || todayStr;
                    let endVal = depDate || startVal;
                    if (new Date(endVal) < new Date(startVal)) { endVal = startVal; }
                    startInput.value = startVal;
                    endInput.value = endVal;
                }, { once: true });
            } else {
                destinationSelect.innerHTML = '<option value="">No destinations available</option>';
            }
        })
        .catch(error => {
            console.error('Error loading destinations:', error);
            destinationSelect.innerHTML = '<option value="">Error loading destinations</option>';
        });
}

// Form submission with payment processing
document.getElementById('campaignRequestForm').addEventListener('submit', function(e) {
    e.preventDefault();
    
    // Validate form
    const form = this;
    const requiredFields = ['campaign_name', 'destination_id', 'target_audience', 'message_content', 'start_date', 'end_date', 'payment_method'];
    let isValid = true;
    
    requiredFields.forEach(field => {
        const input = form.querySelector(`[name="${field}"]`);
        if (!input || !input.value.trim()) {
            isValid = false;
            input.classList.add('border-red-500');
            if (input.nextElementSibling && !input.nextElementSibling.classList.contains('error-message')) {
                const errorDiv = document.createElement('div');
                errorDiv.className = 'error-message text-red-400 text-sm mt-1';
                errorDiv.textContent = 'This field is required';
                input.parentNode.insertBefore(errorDiv, input.nextSibling);
            }
        } else {
            input.classList.remove('border-red-500');
            const errorMsg = input.parentNode.querySelector('.error-message');
            if (errorMsg) errorMsg.remove();
        }
    });
    
    if (!isValid) {
        alert('Please fill in all required fields.');
        return;
    }
    
    const formData = new FormData(form);
    const data = Object.fromEntries(formData);
    
    // Add tier to data
    data.tier = selectedTier;
    
    // Get package cost info
    const packageInfo = displayPackageDetails();
    if (packageInfo) {
        data.calculated_cost = packageInfo.totalCost;
        data.estimated_messages = packageInfo.maxMessages;
        data.package_name = currentTierName;
    }
    
    // Show loading state
    document.getElementById('submitButton').disabled = true;
    document.getElementById('submitText').classList.add('hidden');
    document.getElementById('submitLoading').classList.remove('hidden');
    
    // Normalize optional phone before submit
    if (data.payer_phone) {
        data.payer_phone = normalizeKePhone(data.payer_phone);
    }

    // Submit the form with payment
    fetch('<?php echo base_url('/travel/campaign-request'); ?>', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
        },
        body: JSON.stringify(data)
    })
    .then(response => {
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }
        return response.json();
    })
    .then(result => {
        console.log('Campaign request result:', result);
        if (result.success) {
            if (result.payment_required && result.payment_url) {
                // Redirect to payment page
                window.location.href = result.payment_url;
            } else {
                alert('Campaign request submitted successfully! Our admin team will review and approve your request.');
                closeCampaignRequestModal();
            }
        } else {
            alert('Error submitting campaign request: ' + (result.message || 'Unknown error'));
        }
    })
    .catch(error => {
        console.error('Error:', error);
        alert('Error submitting campaign request. Please try again. Error: ' + error.message);
    })
    .finally(() => {
        // Reset button state
        document.getElementById('submitButton').disabled = false;
        document.getElementById('submitText').classList.remove('hidden');
        document.getElementById('submitLoading').classList.add('hidden');
    });
});

