// Display package details
function displayPackageDetails() {
    if (!currentPricing || !currentTierName) return;
    
    // Calculate package cost (maximum messages × price per SMS)
    const maxMessages = currentPricing.max_messages || 10000; // Default to 10000 if unlimited
    const packageCost = maxMessages * currentPricing.price_per_sms;
    
    // Update display
    document.getElementById('packageName').textContent = currentTierName;
    document.getElementById('pricePerSms').textContent = `KES ${currentPricing.price_per_sms.toFixed(2)}`;
    document.getElementById('maxMessages').textContent = currentPricing.max_messages ? currentPricing.max_messages.toLocaleString() : 'Unlimited';
    document.getElementById('packageCost').textContent = `KES ${packageCost.toFixed(2)}`;
    document.getElementById('totalCost').textContent = `KES ${packageCost.toFixed(2)}`;
    
    return {
        pricePerSms: currentPricing.price_per_sms,
        maxMessages: maxMessages,
        totalCost: packageCost
    };
}

// Kenyan phone normalizer to 2547XXXXXXXX
function normalizeKePhone(input) {
    let digits = (input || '').replace(/\D+/g, '');
    if (digits.startsWith('0')) digits = '254' + digits.substring(1);
    if (digits.startsWith('7') && digits.length === 9) digits = '254' + digits;
    if (digits.startsWith('254') && digits.length === 12) return digits;
    return digits;
}

